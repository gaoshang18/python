# 计算 0~100之间所有偶数的累计求和结果
# 1.定义一个整数的变量记录循环的次数
i = 0
result = 0
while i <= 100:
    # 判断变量 i中的数值是否是一个偶数
    # i % 2 == 0
    # 方法一
    if (i % 2) == 0:
        print("零到100之间的偶数%d" % i)
        result += i
    i += 1
